package JAVA;

public class L8_0Array {
    public static void main(String[] args)
    {
        int num[] = new int[4];
        num[0] = 3;
        num[1] = 4;
        num[2] = 5;
        num[3] = 6;
        for(int i = 0; i < 4; i++)
        {
            System.out.println(num[i]);
        }
    }
}
